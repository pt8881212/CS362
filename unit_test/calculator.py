# Function to addition  
def add(num1, num2): 
    return num1 + num2 
  
# Function to subtraction 
def subtract(num1, num2): 
    return num1 - num2 
  
# Function to multiplication 
def multiply(num1, num2): 
    return num1 * num2 
  
# Function to division
def divide(num1, num2): 
    return num1 / num2 
